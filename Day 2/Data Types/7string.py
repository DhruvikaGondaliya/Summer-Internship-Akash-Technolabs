name = "Dhruvika Gondaliya"

#print complete string
print("Name is : ", name)

#print single character of the string
print(name[0])
print(name[8])
print(name[-4])

#print some characters
print(name[4:8])
print(name[2:5])

#print * string
print(name[3:])
print(name[:10])
print(name[:])

#print some interval of the string
print(name[3::2])
print(name[:15:3])

#print string two times
print(name * 3)

#print concated string
print("Hello " + name + '....')